esp_mqtt_client_handle_t start_wifi_mqtt(void) {
    // Initialize NVS
    esp_err_t ret = nvs_flash_init();
    if (ret == ESP_ERR_NVS_NO_FREE_PAGES || ret == ESP_ERR_NVS_NEW_VERSION_FOUND) {
        ESP_ERROR_CHECK(nvs_flash_erase());
        ret = nvs_flash_init();
    }
    ESP_ERROR_CHECK(ret);

    // Initialize and connect to WiFi
    wifi_init_sta();

    // Wait for WiFi connection
    xEventGroupWaitBits(s_wifi_event_group, WIFI_CONNECTED_BIT, false, true, portMAX_DELAY);

    // Start the MQTT connection
    return mqtt_app_start();

}



void send_metric(esp_mqtt_client_handle_t client, const char* name, long metric) {
    char topic[128];
    snprintf(topic, sizeof(topic), "%s/%s", MQTT_ROOT_TOPIC, name);
    
    
    char output[64];
    snprintf(output, sizeof(output), "%ld", metric);
    printf("%s\t%s\n", topic, output);
    esp_mqtt_client_publish(client, topic, output, 0, 1, 0);



}